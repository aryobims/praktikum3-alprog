#include <iostream>
using namespace std;

int main (){
	int a = 8, b = 3;
	bool hasil;
	
	hasil = a > b;
	cout << "a > b = " << hasil << endl;
	
	hasil = a < b;
	cout << "a < b = " << hasil << endl;
	
	hasil = a >= b;
	cout << "a >= b = " << hasil << endl;
	
	hasil = a <= b;
	cout << "a <= b = " << hasil << endl;
	
	hasil = a == b;
	cout << "a == b = " << hasil << endl;
	
	hasil = a != b;
	cout << "a != b = " << hasil << endl;

}